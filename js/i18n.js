/* ── CURRENT LANGUAGE STATE ─────────────────── */
let currentLang = 'en';

// Expose globals so main.js can access current language translations
window.translations = translations;
Object.defineProperty(window, 'currentLang', {
  get: () => currentLang,
  set: (v) => { currentLang = v; }
});

/* ── APPLY TRANSLATIONS ─────────────────────── */
function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;

  // Text nodes via data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Placeholder attributes via data-i18n-ph
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) {
      el.setAttribute('placeholder', t[key]);
    }
  });

  // Select option text via data-i18n-opt
  document.querySelectorAll('[data-i18n-opt]').forEach(el => {
    const key = el.getAttribute('data-i18n-opt');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Update lang toggle button labels
  const label = document.getElementById('langLabel');
  const labelMobile = document.getElementById('langLabelMobile');
  if (label) label.textContent = t['lang_btn'];
  if (labelMobile) labelMobile.textContent = t['lang_btn'];

  // Update html lang attribute
  document.documentElement.setAttribute('lang', lang);

  // Update page title
  document.title = lang === 'es'
    ? 'TBD Marketing Solutions | Socio de Crecimiento para Restauración y Ajustadores Públicos'
    : 'TBD Marketing Solutions | Growth Partner for Restoration Companies & Public Adjusters';
}

/* ── TOGGLE HANDLER ─────────────────────────── */
function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'es' : 'en';
  applyTranslations(currentLang);

  // Animate the button
  const btns = document.querySelectorAll('.lang-toggle');
  btns.forEach(btn => {
    btn.classList.add('lang-switching');
    setTimeout(() => btn.classList.remove('lang-switching'), 300);
  });

  // Save preference
  try { localStorage.setItem('tbd_lang', currentLang); } catch(e) {}
}

/* ── INIT ────────────────────────────────────── */
document.addEventListener('DOMContentLoaded', () => {
  // Restore saved preference
  try {
    const saved = localStorage.getItem('tbd_lang');
    if (saved && translations[saved]) currentLang = saved;
  } catch(e) {}

  // Wire up toggle buttons
  const langToggle = document.getElementById('langToggle');
  const langToggleMobile = document.getElementById('langToggleMobile');
  if (langToggle) langToggle.addEventListener('click', toggleLanguage);
  if (langToggleMobile) langToggleMobile.addEventListener('click', () => {
    toggleLanguage();
    // Close mobile nav after toggle
    document.getElementById('hamburger').classList.remove('open');
    document.getElementById('mobileNav').classList.remove('open');
  });

  // Apply on load if non-English saved
  if (currentLang !== 'en') applyTranslations(currentLang);
});
